package com.Syntax_Class07;

public class Session07ProgrammingFeb6 {

	public static void main(String[] args) {
		// incremental and decremental
		
		
		int num=10;
		
		num=num+1;
		
		num+=1;
		
		num++;
		System.out.println(num);
		
		// increment operator add 1 to a variable.
		
		num++;
		
		System.out.println(num);
		
		// increment operator doesn't work with numbers
		// 10++ // this is invalid argument 
		
		
		num--;
		num--;
		num--;
		
		System.out.println(num);
		
		int time=11;
		if (time<12) {
			System.out.println("Morning");
		}
		
		while (time<12) {
			System.out.println("Morning");
		}
		
		
	}

}
