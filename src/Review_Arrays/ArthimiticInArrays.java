package Review_Arrays;

public class ArthimiticInArrays {

	public static void main(String[] args) {

		
		for (int i=1; i<10; i++) {
			
			for (int x=1; x<10; x++) {
				System.out.println(i+"x"+x+"="+i*x);
			}
		}
		
		
		System.out.println("________________________");
		
		for (int a=0; a<10; a++) {
			for(int c=0; c<10;c++) {
				
				System.out.println(a+ "+"+c+"="+c);
				
			}
		}
	}

}
